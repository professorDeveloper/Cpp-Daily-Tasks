#include <iostream>
using namespace std;

int main() {
    for (int i = 14; i <= 20; i++) {
        cout << i << endl;
    }
    for (int i = 11; i <= 99; i++) {
        cout << i << endl;
    }
    for (int i = 12; i <= 200; i++) {
        cout << i << endl;
    }
    for (int i = 25; i >= 10; i--) {
        cout << i << endl;
    }
    for (int i = 500; i >= 80; i--) {
        cout << i << endl;
    }
}
